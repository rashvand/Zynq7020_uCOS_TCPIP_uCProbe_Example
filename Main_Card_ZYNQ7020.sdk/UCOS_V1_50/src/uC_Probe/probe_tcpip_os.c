/*
*********************************************************************************************************
*                                        ProbeTCPIP_OS_Init()
*
* Description : Initialize the UDP server task for Probe communication.
*
* Argument(s) : none.
*
* Return(s)   : none.
*
* Caller(s)   : ProbeTCPIP_Init().
*
* Note(s)     : none.
*********************************************************************************************************
*/

void  ProbeTCPIP_OS_Init (void)
{
    OS_ERR  err;


    OSTaskCreate((OS_TCB     *)&ProbeTCPIP_OS_TaskTCB,
                 (CPU_CHAR   *)"Probe TCPIP",
                 (OS_TASK_PTR ) ProbeTCPIP_OS_Task,
                 (void       *) 0,
                 (OS_PRIO     ) PROBE_TCPIP_CFG_TASK_PRIO,
                 (CPU_STK    *)&ProbeTCPIP_OS_TaskStk[0],
                 (CPU_STK_SIZE)(PROBE_TCPIP_CFG_TASK_STK_SIZE / 10u),
                 (CPU_STK_SIZE) PROBE_TCPIP_CFG_TASK_STK_SIZE,
                 (OS_MSG_QTY  ) 0u,
                 (OS_TICK     ) 0u,
                 (void       *) 0,
                 (OS_OPT      )(OS_OPT_TASK_STK_CHK | OS_OPT_TASK_STK_CLR),
                 (OS_ERR     *)&err);
    
    (void)&err;
}


/*
*********************************************************************************************************
*                                         ProbeTCPIP_OS_Dly()
*
* Description : Delay the UDP server task.
*
* Argument(s) : ms          Number of milliseconds for which the UDP task should be delayed.
*
* Return(s)   : none.
*
* Caller(s)   : ProbeTCPIP_Task().
*
* Note(s)     : none.
*********************************************************************************************************
*/

void  ProbeTCPIP_OS_Dly (CPU_INT16U  ms)
{
    OS_ERR      err;

    
    OSTimeDlyHMSM((CPU_INT16U) 0u,
                  (CPU_INT16U) 0u,
                  (CPU_INT16U) 0u,
                  (CPU_INT32U) ms,
                  (OS_OPT    ) OS_OPT_TIME_HMSM_NON_STRICT,
                  (OS_ERR   *)&err);
}


